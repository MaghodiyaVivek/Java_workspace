public class Program10 
{

	//check the given number is odd or even.
	
	public static void main(String[] args) 
	{
		
		
		int num = 5;
		int v = num%2;
		if(v==0)
		{
			System.out.println("Number is even");
		}
		else
		{
			System.out.println("Number is Odd");
		}
	
	
	}


}
